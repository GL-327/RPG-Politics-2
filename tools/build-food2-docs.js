/**
 * Generates docs/expansion2/food.md from food2-roster.js.
 * Usage: node tools/build-food2-docs.js
 */
const fs = require('fs');
const path = require('path');
const { ROSTER } = require('./food2-roster');

function categoryOf(e) {
  const id = e.id;
  if (e.kind === 'drink') return 'Drinks';
  if (['feast_platter','warrior_stew','hunter_rations','miner_breakfast','hero_sandwich','phoenix_roast','dragon_noodles','knight_feast','sea_kings','golden_curry','elven_salad','diplomat_pastries'].some(k => id.includes(k))) return 'Buff meals';
  if (['hot_dog','soft_pretzel','nachos','quesadilla','empanada','loaded_fries','corn_dog','poutine','tacos_al','arepa','gyros','pretzel_dog','street_corn','elote','jerk_skewer'].some(k => id.includes(k))) return 'Street food';
  if (['tiramisu','creme_brulee','macaron','baklava','knafeh','mochi','flan','pavlova','cannoli','panna_cotta','gelato','sorbet','cupcake','brownie','cheesecake','fudge','truffle','eclair','churros','sticky_toffee','banoffee','pecan','lava_cake','mille_feulle','pastry_assortment'].some(k => id.includes(k))) return 'Desserts';
  if (['hummus','falafel','shawarma','kebab','tabouleh','baba','pita','tagine','kofta','fattoush','mansaf','kibbeh','mujadara','manakish','grape_leaves','shakshuka','ful_medames','labneh','persian','turkish_delight','pomegranate','tahini','mehze','harira','manti'].some(k => id.includes(k))) return 'Middle Eastern';
  if (['paella','ratatouille','quiche','croissant','baguette','gnocchi','lasagna','carbonara','goulash','pierogi','borscht','schnitzel','bratwurst','fish_chips','shepherd','moussaka','spanakopita','cottage','welsh','scotch','beef_wellington','coq','bouillabaisse','cassoulet','souvlaki','tapas','ribollita','cacciatore'].some(k => id.includes(k))) return 'European';
  return 'Asian';
}

function fmtEffects(e) {
  if (!e.effects || e.effects.length === 0) return '—';
  return e.effects.map((x) => {
    const [name, sec, amp] = x.split(':');
    const label = name.charAt(0) + name.slice(1).toLowerCase().replace(/_/g, ' ');
    return amp && amp !== '0' ? `${label} ${sec}s II` : `${label} ${sec}s`;
  }).join(' + ');
}

const byCat = {};
for (const e of ROSTER) {
  const c = categoryOf(e);
  (byCat[c] ||= []).push(e);
}

const catOrder = ['Asian', 'European', 'Middle Eastern', 'Desserts', 'Drinks', 'Street food', 'Buff meals'];

let tables = '';
for (const cat of catOrder) {
  const items = byCat[cat] || [];
  if (!items.length) continue;
  tables += `\n### ${cat} (${items.length})\n\n`;
  tables += '| Item | Hunger | Sat. | Effects |\n|------|:------:|:----:|---------|\n';
  for (const e of items) {
    tables += `| ${e.name} | ${e.n} | ${e.s} | ${fmtEffects(e)} |\n`;
  }
}

const buffCount = ROSTER.filter((e) => e.effects && e.effects.length).length;
const drinkCount = ROSTER.filter((e) => e.kind === 'drink').length;

const md = `# Food Expansion — Phase 2 (Global Cuisine)

Croptopia-scale roster of **${ROSTER.length} edible items** covering Asian, European,
Middle Eastern, dessert, drink, street-food and hero-tier buff meals. Everything is a
plain vanilla \`Item\` with \`FoodProperties\`; ${buffCount} items attach a \`Consumable\`
for short vanilla status effects (${drinkCount} drinks use the drink animation and are
always edible).

- **Package:** \`com.political.expansion2.food\`
- **Entry class:** \`Food2Items\` — exposes \`register()\` and \`items()\`
- **Item id prefix:** \`food2_\`
- **Mod id / namespace:** \`politicalserver\`
- No mixins, no Phase-1 \`com/political/expansion/food/**\` edits, no crop blocks.

## Integration (what the integration agent must wire)

1. **Register** during mod init (server/common), next to Phase-1 food:

   \`\`\`java
   com.political.expansion2.food.Food2Items.register();
   \`\`\`

2. **Creative tab** — iterate \`Food2Items.items()\` (stable declaration order):

   \`\`\`java
   for (net.minecraft.world.item.Item item : com.political.expansion2.food.Food2Items.items()) {
       out.accept(new net.minecraft.world.item.ItemStack(item));
   }
   \`\`\`

   Add to an existing consumables tab or create **"RPG — Global Cuisine"**.

3. **Translations** — merge \`tools/lang-fragments/food2.json\` into
   \`assets/politicalserver/lang/en_us.json\`. Keys are \`item.politicalserver.food2_*\`.

4. **Textures / models** — generated by \`node tools/gen-food2.js\` (jimp). Each item has:
   - \`textures/item/<id>.png\`
   - \`models/item/<id>.json\`
   - \`items/<id>.json\`

   Source roster: \`tools/food2-roster.js\`. Regenerate Java/lang via
   \`node tools/build-food2-java.js\`.

## Category summary

| Category | Count |
|----------|------:|
${catOrder.map((c) => `| ${c} | ${(byCat[c] || []).length} |`).join('\n')}
| **Total** | **${ROSTER.length}** |

## Full roster
${tables}

## Notes

- Effects use vanilla \`MobEffects\` via \`ApplyStatusEffectsConsumeEffect\` (1.21.2+ \`Consumable\`).
- Amplifiers are level 0 unless noted; hero-tier buff meals stack 2–3 gentle effects.
- Phase-1 \`food_*\` items remain untouched; Phase-2 ids are exclusively \`food2_*\`.
`;

const outPath = path.join(__dirname, '..', 'docs', 'expansion2', 'food.md');
fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, md);
console.log('docs/expansion2/food.md written for', ROSTER.length, 'items');
